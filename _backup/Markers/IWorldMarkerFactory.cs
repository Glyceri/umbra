using System.Collections.Generic;

namespace Umbra.Markers;

public interface IWorldMarkerFactory
{
    /// <summary>
    /// Returns a list of world markers.
    /// </summary>
    /// <remarks>
    /// This method is called every frame to render the world markers. It is
    /// up to the implementation to decide how to create and cache the markers
    /// if necessary. The implementation should also take care to only return
    /// markers that should be visible in the current map.
    /// </remarks>
    public List<WorldMarker> GetMarkers();
}
